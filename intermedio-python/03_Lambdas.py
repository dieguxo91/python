# lambda

# funciones en una variable que luego la podemos usar como una funcion
sum_two_values = lambda first_value, second_value : first_value + second_value

print(sum_two_values(2,4))

print_value = lambda value : print(value)

print_value(5)

multiples_values = lambda first_value, second_value : first_value * second_value 

print(multiples_values(5, 6))